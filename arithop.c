//Simple arithmetic operators
#include<stdio.h>
void main()
{
	int num1, num2;
	printf("Enter the two numbers\n");
	scanf("%d %d", &num1, &num2);
	
	printf("Basic Arithmethic operations are:\n");
	printf("num1+num2= %d\n", (num1+num2));
	printf("num1-num2= %d\n", (num1-num2));
	printf("num1*num2= %d\n", (num1*num2));
	printf("num1/num2= %d\n", (num1/num2));
	printf("num1%%num2= %d\n", (num1%num2));
	
	
}
